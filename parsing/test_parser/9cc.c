//consume with operand (en gardant des memoires)
bool consume(char *op) {
  if (token->kind != TK_RESERVED ||
      strlen(op) != token->len ||
      memcmp(token->str, op, token->len))
    return false;
  token = token->next;
  return true;
}

bool consume_indent(void) {
  if (token->kind != TK_IDENT || token->str[0] != op)
    return false;
  token = token->next;
  return true;
}

