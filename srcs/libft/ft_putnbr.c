#include "libft.h"

void	ft_putnbr(int nb)
{
	char	c;
	long	a;

	a = nb;
	if (a < 0)
	{
		write(1, "-", 1);
		a = a * -1;
	}
	if (a >= 10)
	{
		ft_putnbr(a / 10);
		ft_putnbr(a % 10);
	}
	else
	{
		c = a + '0';
		write(1, &c, 1);
	}
}
